package org.nutriGuideBuddy.features.user.enums;

public enum DietType {
  STANDARD, // balanced RDI-based
  KETO, // high fat, very low carb
  PALEO, // no grains/legumes, more protein/fat
  VEGAN, // plant-only, nutrient supplementation needed
  VEGETARIAN, // no meat, allows dairy/eggs
  MEDITERRANEAN, // high unsaturated fat, fiber
  DASH, // low sodium, high potassium/calcium/magnesium
  LOW_CARB // moderate carb restriction, not full keto
}
